package Week1HW;

public class Conditionals {
    public static void main(String[] args) {

        int sayi = 13;

        if (sayi < 12) {
            System.out.println("Sayı 12'den küçüktür.");
        } else if (sayi == 12) {
            System.out.println("Sayı 12'ye eşittir.");
        } else System.out.println("Sayı 12'den büyüktür.");
    }
}
